package com.example.revelio.models;

public class YelpModel {
	String name = "";
	String mobile_url = "";
	String display_address = "";
	String title = "";
	String phone_number ="";
	String snippet_text = "";
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMobile_url() {
		return mobile_url;
	}
	public void setMobile_url(String mobile_url) {
		this.mobile_url = mobile_url;
	}
	public String getDisplay_address() {
		return display_address;
	}
	public void setDisplay_address(String display_address) {
		this.display_address = display_address;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getPhone_number() {
		return phone_number;
	}
	public void setPhone_number(String phone_number) {
		this.phone_number = phone_number;
	}
	public String getSnippet_text() {
		return snippet_text;
	}
	public void setSnippet_text(String snippet_text) {
		this.snippet_text = snippet_text;
	}

	
	
}
